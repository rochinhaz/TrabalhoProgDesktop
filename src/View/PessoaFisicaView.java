    private void jButtonConsultarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonConsultarMouseClicked

    String cpf = jTextFieldConsulta.getText().trim();

        if (cpf.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, digite um CPF válido.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        PessoaFisica pessoaFisicaConsultada = pessoaFisicaTela.getPessoaFisica(cpf);

        if (pessoaFisicaConsultada != null) {
            JOptionPane.showMessageDialog(this, "Pessoa Fisica encontrada:\nCpf: " + pessoaFisicaConsultada.getCpf() + "\nNome: " + pessoaFisicaConsultada.getNome() + "\nTelefone: " + pessoaFisicaConsultada.getTelefone() + "\nEndereco: " + pessoaFisicaConsultada.getEndereco(), "Consulta de Pessoa Fisica", JOptionPane.INFORMATION_MESSAGE);

            jTextFieldCpf.setText(pessoaFisicaConsultada.getCpf());
            jTextFieldNome.setText(pessoaFisicaConsultada.getNome());
            jTextFieldTelefone.setText(pessoaFisicaConsultada.getTelefone());
            jTextFieldEndereco.setText(pessoaFisicaConsultada.getEndereco());
        } else {
            JOptionPane.showMessageDialog(this, "Pessoa Fisica com o CPF " + cpf + " não encontrada.", "Erro", JOptionPane.ERROR_MESSAGE);

            jTextFieldCpf.setText("");
            jTextFieldNome.setText("");
            jTextFieldTelefone.setText("");
            jTextFieldEndereco.setText("");
        }
    }//GEN-LAST:event_jButtonConsultarMouseClicked

