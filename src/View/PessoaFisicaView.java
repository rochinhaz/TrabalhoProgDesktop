    public void addInTable(){
        
        DefaultTableModel modelo = (DefaultTableModel) tbPessoaFisica.getModel();
        int aux = 0;
        modelo.setRowCount(aux);
        for(PessoaFisica pessoaFisica : pessoaFisicaTela.getTodasPessoasFisica()){
            modelo.insertRow(aux, new Object []{pessoaFisica.getNome(), pessoaFisica.getCpf(),pessoaFisica.getTelefone(),pessoaFisica.getEndereco()});
            aux++;
        }
    }

