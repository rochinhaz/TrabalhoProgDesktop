    private void jButtonAdicionarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonAdicionarMouseClicked
        PessoaFisica pessoaFisica = new PessoaFisica();
        pessoaFisica.setNome(jTextFieldNome.getText());
        pessoaFisica.setCpf(jTextFieldCpf.getText());
        pessoaFisica.setTelefone(jTextFieldTelefone.getText());
        pessoaFisica.setEndereco(jTextFieldEndereco.getText());
        
        if(pessoaFisicaTela.savePessoaFisica(pessoaFisica)){
            JOptionPane.showMessageDialog(null, "Pessoa Fisica cadastrada com sucesso", "SUCESSO", JOptionPane.DEFAULT_OPTION);
            addInTable();
        } else {
            JOptionPane.showMessageDialog(null, "Pessoa Fisica com CPF já cadastrado", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonAdicionarMouseClicked

