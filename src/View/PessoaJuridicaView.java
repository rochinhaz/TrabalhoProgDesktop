    private void jButtonConsultarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonConsultarMouseClicked
    String cnpj = jTextFieldConsulta.getText().trim();

        if (cnpj.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, digite um CNPJ válido.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        PessoaJuridica pessoaJuridicaConsultada = pessoaJuridicaTela.getPessoaJuridica(cnpj);

        if (pessoaJuridicaConsultada != null) {
            JOptionPane.showMessageDialog(this, "Pessoa Juridica encontrada:\nCNPJ: " + pessoaJuridicaConsultada.getCnpj() + "\nNome: " + pessoaJuridicaConsultada.getNome() + "\nTelefone: " + pessoaJuridicaConsultada.getTelefone() + "\nEndereco: " + pessoaJuridicaConsultada.getEndereco(), "Consulta de Pessoa Juridica", JOptionPane.INFORMATION_MESSAGE);

            jTextFieldCnpj.setText(pessoaJuridicaConsultada.getCnpj());
            jTextFieldNome.setText(pessoaJuridicaConsultada.getNome());
            jTextFieldTelefone.setText(pessoaJuridicaConsultada.getTelefone());
            jTextFieldEndereco.setText(pessoaJuridicaConsultada.getEndereco());
        } else {
            JOptionPane.showMessageDialog(this, "Pessoa Juridica com o CNPJ " + cnpj + " não encontrada.", "Erro", JOptionPane.ERROR_MESSAGE);

            jTextFieldCnpj.setText("");
            jTextFieldNome.setText("");
            jTextFieldTelefone.setText("");
            jTextFieldEndereco.setText("");
        }
    }//GEN-LAST:event_jButtonConsultarMouseClicked

