    public void addInTable(){
        
        DefaultTableModel modelo = (DefaultTableModel) tbPessoaJuridica.getModel();
        int aux = 0;
        modelo.setRowCount(aux);
        for(PessoaJuridica pessoaJuridica : pessoaJuridicaTela.getTodasPessoasJuridica()){
            modelo.insertRow(aux, new Object []{pessoaJuridica.getNome(), pessoaJuridica.getCnpj(),pessoaJuridica.getTelefone(),pessoaJuridica.getEndereco()});
            aux++;
        }
    }

