    private void jButtonAdicionarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonAdicionarMouseClicked
       PessoaJuridica pessoaJuridica = new PessoaJuridica();
        pessoaJuridica.setNome(jTextFieldNome.getText());
        pessoaJuridica.setCnpj(jTextFieldCnpj.getText());
        pessoaJuridica.setTelefone(jTextFieldTelefone.getText());
        pessoaJuridica.setEndereco(jTextFieldEndereco.getText());
        
        if(pessoaJuridicaTela.savePessoaJuridica(pessoaJuridica)){
            JOptionPane.showMessageDialog(null, "Pessoa Juridica cadastrada com sucesso", "SUCESSO", JOptionPane.DEFAULT_OPTION);
            addInTable();
        } else {
            JOptionPane.showMessageDialog(null, "Pessoa Juridica com CNPJ já cadastrado", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonAdicionarMouseClicked

