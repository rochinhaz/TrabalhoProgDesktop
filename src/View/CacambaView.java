    private void jButtonAdicionarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonAdicionarMouseClicked
        Cacamba cacamba = new Cacamba();
        cacamba.setId(Integer.parseInt(jTextFieldNumero.getText()));
        cacamba.setCondicao(jTextFieldCondicao.getText());
        
        if(cacambaTela.saveCacamba(cacamba)){
            JOptionPane.showMessageDialog(null, "Cacamba cadastrada com sucesso", "SUCESSO", JOptionPane.DEFAULT_OPTION);
            addInTable();
        } else {
            JOptionPane.showMessageDialog(null, "Cacamba com numero já cadastrado", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonAdicionarMouseClicked

    private void jButtonExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonExcluirMouseClicked
        if(cacambaTela.deleteCacamba(Integer.valueOf(jTextFieldNumero.getText()))){
            JOptionPane.showMessageDialog(null, "Cacamba deletada com sucesso", "SUCESSO", JOptionPane.DEFAULT_OPTION);
            addInTable();
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao deletar cacamba", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonExcluirMouseClicked

    private void jButtonEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonEditarMouseClicked
        Cacamba cacamba = new Cacamba();
        cacamba.setId(Integer.parseInt(jTextFieldNumero.getText()));
        cacamba.setCondicao(jTextFieldCondicao.getText());
        
        if(cacambaTela.updateCacamba(cacamba)){
            JOptionPane.showMessageDialog(null, "Cacamba atualizada com sucesso", "SUCESSO", JOptionPane.DEFAULT_OPTION);
            addInTable();
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao atualizar cacamba", "ERRO", JOptionPane.ERROR_MESSAGE); 
        }
    }//GEN-LAST:event_jButtonEditarMouseClicked

    private void jButtonConsultarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonConsultarMouseClicked
                  
            String inputText = jTextFieldConsulta.getText().trim();

        if (inputText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, digite um número válido.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            int id = Integer.parseInt(inputText);
            Cacamba cacambaConsultada = cacambaTela.getCacamba(id);

            if (cacambaConsultada != null) {
                JOptionPane.showMessageDialog(this, "Cacamba encontrada:\n Numero: " + cacambaConsultada.getId() + "\nCondição: " + cacambaConsultada.getCondicao(), "Consulta de Cacamba", JOptionPane.INFORMATION_MESSAGE);
                jTextFieldNumero.setText(String.valueOf(cacambaConsultada.getId()));
                jTextFieldCondicao.setText(cacambaConsultada.getCondicao());
            } else {
                JOptionPane.showMessageDialog(this, "Cacamba com o Numero " + id + " não encontrada.", "Erro", JOptionPane.ERROR_MESSAGE);
                jTextFieldNumero.setText("");
                jTextFieldCondicao.setText("");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, digite um número válido.", "Erro", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_jButtonConsultarMouseClicked

