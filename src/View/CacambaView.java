    public void addInTable(){
        
        DefaultTableModel modelo = (DefaultTableModel) tbCacamba.getModel();
        int aux = 0;
        modelo.setRowCount(aux);
        for(Cacamba cacamba : cacambaTela.getTodasCacambas()){
            modelo.insertRow(aux, new Object []{cacamba.getId(), cacamba.getCondicao()});
            aux++;
        }
    }

